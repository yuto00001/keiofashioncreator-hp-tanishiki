$(function () {
  $(".20").on("click", function () {
    const p20 = document.getElementById("p20");
    if (p20.style.display == "none") {
      // blockで表示
    $(p20).fadeIn();
      p20.style.display = "flex";
    } else {
      // noneで非表示
    $(p20).fadeOut();
      p20.style.display = "none";
    }
  });
});
